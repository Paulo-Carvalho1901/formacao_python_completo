"""
Repetição
while (enquanto)
Executar uma ação enqunto uma condição for verdadeira
Loop infinito quando um código não tem fim 
"""

contador = 0 # setando o incio da variavel em zero

while contador < 10: # criando a condição de check do código
    contador = contador + 1 # encrementando a variavel em 1
    print(contador) # mostrando o resultado
    

print('Acabou!')
